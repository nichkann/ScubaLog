- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"LogBookListCell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier];
    }
    
    //cell.textLabel.text = @"TestText";
    ScubaLog *_scubaLog = [self.dataModel.scubaLogList objectAtIndex:indexPath.row];
    cell.textLabel.text = _scubaLog.name;
    cell.detailTextLabel.text = @"detail text";
    cell.imageView.image = [UIImage imageNamed:@"first.png"];
    
    return cell;
}

